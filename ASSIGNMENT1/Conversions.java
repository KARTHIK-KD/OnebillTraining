package com.onebill.ty.assignment.day2;

public class Conversions {
	public static void main(String[] args) {
		//Automatic/Widening
		int value=10;
		float value2=value;
		System.out.println(value+" "+value2);
		//Type Casting
		value=(int)value2;
		System.out.println(value+" "+value2);
	}

}
