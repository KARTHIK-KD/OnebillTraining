package sampe;

public class StringSwitch {
	public static void main(String[] args) {
		String s="karthik";
		switch(s) {
		case "karthik":System.out.println("this is karthik");break;
		case "mani":System.out.println("this is mani");break;
		default:System.out.println("wrong name");
		}
	}

}
